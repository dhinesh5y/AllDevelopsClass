package com.conditions.day5;

public class CheckVowelsInChar {

	public static void main(String[] args) {
		
		
		char ch = 'E';
		
		if (ch=='A'||ch=='E'||ch=='I'||ch=='O'||ch=='U') {
			System.out.println(ch+" its a vowel");
			
		} else {
			System.out.println(ch+" its not a vowel");

		}

	}

}
