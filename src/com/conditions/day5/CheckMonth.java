package com.conditions.day5;

public class CheckMonth {

	public static void main(String[] args) {

		String month = "August";
		switch (month) {
		case "January":
			System.out.println(month + " 1st month");
			break;
		case "February":
			System.out.println(month + " 2nd month");
			break;
		case "March":
			System.out.println(month + " 3rd month");
			break;
		case "April":
			System.out.println(month + " 4th month");
			break;
		case "May":
			System.out.println(month + " 5th month");
			break;
		case "June":
			System.out.println(month + " 6th month");
			break;
		case "July":
			System.out.println(month + " 7th month");
			break;
		case "August":
			System.out.println(month + " 8th month");
			break;
		case "September":
			System.out.println(month + " 9th month");
			break;
		case "October":
			System.out.println(month + " 10th month");
			break;
		case "November":
			System.out.println(month + " 11th month");
			break;
		case "December":
			System.out.println(month + " 12th month");
			break;
		default:
			System.out.println(month + " is invalid");
			break;
		}

	}

}
