package com.oops2.Iherit.poly.day11;

public class Adder {
	
	public int addThis(int a, int b, int c){
		return a+b+c;
		
	}
	public String addThis(char a, char b){
		return a+""+b;
		
	}
	public long addThis(long a, long b){
		return a+b;
	}
	public String  addThis(String a, String b, String c){
		return a+b+c;
	}
	public float addThis(float a, float b){
		return a+b;
	}

}
